//! Helper module for all sorts of fun art-related things for flippy!

use std::time::{SystemTime, UNIX_EPOCH};

macro_rules! rgb_color {
    ($r:expr, $g:expr, $b:expr, $text:expr) => {
        concat!("\x1b[38;2;", $r, ";", $g, ";", $b, "m", $text)
    };
}

macro_rules! reset {
    ($text: expr) => {
        concat!("\x1b[0m", $text)
    };
}

